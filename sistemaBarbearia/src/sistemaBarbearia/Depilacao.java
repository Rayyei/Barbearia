package sistemaBarbearia;

public class Depilacao extends Barbearia{
	
	private String tiposDepilacao;
	private String localDepilacao;
	private int secoesDepilacao;
	
	public String getTiposDepilacao() {
		return tiposDepilacao;
	}
	public void setTiposDepilacao(String tiposDepilacao) {
		this.tiposDepilacao = tiposDepilacao;
	}
	public String getLocalDepilacao() {
		return localDepilacao;
	}
	public void setLocalDepilacao(String localDepilacao) {
		this.localDepilacao = localDepilacao;
	}
	public int getSecoesDepilacao() {
		return secoesDepilacao;
	}
	public void setSecoesDepilacao(int secoesDepilacao) {
		this.secoesDepilacao = secoesDepilacao;
	}
}
